#!/usr/bin/env node
/**
 * build-manifest.mjs — generate bin/commands/_manifest.cjs.
 *
 * The filesystem-discovery loader in bin/commands/_loader.cjs uses
 * `readdirSync` + dynamic `require(path.join(...))` to enumerate command
 * modules. That pattern is opaque to static bundlers (esbuild, bun build
 * --compile), so when the CLI is packaged into a single executable, none
 * of the command modules get embedded.
 *
 * This script runs as a pre-bundle step. It enumerates bin/commands/*.cjs
 * (skipping underscore-prefixed internal files) and writes a generated
 * _manifest.cjs with explicit static requires. The loader prefers the
 * manifest at runtime and falls back to readdir when the manifest is
 * absent (dev / unbundled contexts).
 *
 * Output is deterministic (sorted), regenerated on every build, and
 * should be gitignored — it's a build artifact.
 */

import { readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const COMMANDS_DIR = join(ROOT, 'bin', 'commands');
const MANIFEST_PATH = join(COMMANDS_DIR, '_manifest.cjs');

function listCommandModules() {
  return readdirSync(COMMANDS_DIR)
    .filter((f) => f.endsWith('.cjs') && !f.startsWith('_'))
    .sort();
}

function generateManifest(files) {
  const lines = [
    "'use strict';",
    '/**',
    ' * GENERATED by scripts/build-manifest.mjs. Do not edit by hand.',
    ' *',
    ' * Static require manifest for bin/commands/*.cjs. Lets static bundlers',
    ' * (esbuild, bun build --compile) trace every command module. Consumed',
    ' * by bin/commands/_loader.cjs when present; filesystem fallback otherwise.',
    ' */',
    '',
    'module.exports = {',
  ];
  for (const file of files) {
    const name = file.replace(/\.cjs$/, '');
    lines.push(`  ${JSON.stringify(name)}: require(${JSON.stringify('./' + file)}),`);
  }
  lines.push('};', '');
  return lines.join('\n');
}

function main() {
  const files = listCommandModules();
  const output = generateManifest(files);
  writeFileSync(MANIFEST_PATH, output, 'utf8');
  console.log(`Wrote command manifest: ${files.length} modules → ${MANIFEST_PATH}`);
}

main();
